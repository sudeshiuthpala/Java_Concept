public class TypeCasting{
    public static void main(String[] args){
        int num=9;
        double num2=num;
        System.out.println(num);
        System.out.println(num2);
        double myDouble=9.87d;
        int myInt=(int)myDouble;
        System.out.println(myDouble);
        System.out.println(myInt);
    }
}